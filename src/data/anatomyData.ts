// Get unique muscle IDs (removing left/right duplicates for UI)
export const getUniqueMuscles = () => {
  const seen = new Set<string>();
  return ANATOMICAL_MUSCLES.filter(m => {
    const baseId = m.id.replace(/-left$/, '');
    if (seen.has(baseId)) return false;
    seen.add(baseId);
    return true;
  });
};
